// 获取植物世界观分类的辅助函数
export const getPlantWorldview = (plant: PlantConfig): string => {
  if (!plant) return "未知"; // 添加安全检查
  
  if (plant.name.includes("徐清岚") && !plant.name.includes("（神）") && !plant.name.includes("（梦）") || 
      plant.name === "屹九" || 
      plant.name.includes("Vexithra") ||
      plant.name === "羽尘") {
    return "人类世界";
  } else if (plant.name === "清鸢" || plant.name === "善•猫教主" || plant.name === "青玄" || plant.name === "奈奈生" || plant.name === "晓月") {
    return "光明大陆";
  } else if (plant.name === "慕容言风" || plant.name === "沐沐") {
    return "暗影大陆";
  } else if (plant.name === "冥界三使" || plant.name === "筱燊傀") {
    return "冥界";
  } else if (plant.name === "凌" || plant.name === "星褶" || plant.name === "灵•瑶凝" || plant.name === "离火双生•长离" || plant.name === "镜花水月·灵汐" || plant.name === "苍玄" || plant.name === "青芜" || plant.name === "双生花仙•灵汐" || plant.name === "凝霜" || plant.name === "玄霜" || plant.name === "灵汐与赤瞳" || plant.name === "星轨三司" || plant.name === "灵璇" || plant.name === "太阴剑主·望舒" || plant.name === "虚无之主·蝶澈") {
    return "洪荒";
  } else if (plant.name === "云璃•时光裁决者" || plant.name === "烬月神女·灵汐") {
    return "命运神殿";
  } else if (plant.name === "灵汐") {
    return "时空城";
  }
  return "未知";
};

